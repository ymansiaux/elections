               print("coucou"),
               ignoreNULL = FALSE)
  
  observeEvent(session, {
    dat <- (xtradata::xtradata_requete_features(key = "DATAZBOUBB", typename = "ST_PARK_P", showURL = TRUE))
    print(dat)
    
  }, ignoreNULL = FALSE)
  
  
